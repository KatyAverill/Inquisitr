	<p><strong>Main Keywords:</strong> Usually two or three words that appear throughout the article and are the major focus point of the article. Example: "Kurt Russell" or "Army tattoo policy".</p>
	<p><strong>Hard Duplicate:</strong> This is an article that duplicates another article's Main Keywords as well as the story and angle.</p>
	<p><strong>Soft Duplicate:</strong> This is an article that duplicates another article's Main Keywords but with a different story and angle.</p><br />
      <p><strong>Here are the current policies and rules to deal with duplicate articles:</strong><br />
      <ul>
			<li>Always search the posts for the Main Keywords using this link http://www.inquisitr.com/wp-admin/edit.php
Go to the upper right corner and enter the Main Keywords and see if any articles were published already. This is the fastest and easiest way to avoid hard duplicates.</li>
			<li>If you plan to write a Hard Duplicate, then ALL the following conditions must be met...
				<ul>
					<li>(1) Another article is not on Google, Yahoo or Bing for the Main Keywords.</li>
					<li>(2) Another article has not been published within the past 8 hours.></li>
				</ul></li>
			<li>If you plan to write a soft Duplicate, then ALL the following conditions must be met...
				<ul>
					<li>(1) Another article is not on Google, Yahoo or Bing for the Main Keywords.</li>
					<li>(2) Another article is not in draft since you won't know the content of it until it's published.</li>
				</ul></li>
			<li>When you draft an article, please wait five minutes then search the posts again for the Main Keywords to be sure no one else drafted before you. Sometimes another writer could be drafting within a minute from you, so if you drafted first then you can go ahead and continue writing, otherwise you would have to move on to another Main Keywords.</li>
			<li>DO NOT draft two articles at once, ever!</li>
	  </ul>

