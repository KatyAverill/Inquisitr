      <p> <strong>The basic rule for people who hate to read: all images should be  670x409 and the bottom 22px should not be important.</strong><br />
      For everyone  else:</p>
      <p> 670x409 is actually just the <em>minimum</em> size images can be.</p>
      <p> There are three main image sizes and breakpoints: </p>
      <ul>
        <li>
          Thumbnails
        </li>
        <li>
          Featured
        </li>
        <li>
          Single Leader
        </li>
      </ul>
      <p> All other sizes are scaled down from these three.      </p>
      Thumbnails are always square and are scaled then cropped into a  square <strong>from the center</strong>.<br />
      Featured images are scaled then cropped <strong>from the center</strong>.<br />
      Single Leader sized images are scaled then <strong>cropped from the top</strong>.</p>
      <p> If you add an image bigger than 670x409, it will scale the width to  670 or the height to 409 then remove the extra image equally from  either the left/right or the top/bottom.</p>
      <p> HOWEVER, for the Single Leader, the image is scaled to the width or  height, then the extra is removed from the sides or ONLY the bottom.</p>
      <p>THEREFORE, when  uploading an image at 670x409, the bottom 22px should be basically  optional!</p><br /></td>
