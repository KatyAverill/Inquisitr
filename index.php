<img src="articleTitleImage1.jpg" />
<p>As you can see, when a reader searches for 'Snoop Dogg', the searched words are highlighted in BOLD on the results. Search engines prefer to display articles with HOT keywords at the start of their titles. Naturally those titles will also get the most click throughs. Search engines love articles that get the most click throughs because it means they have done well presenting accurate information to the search crowd. You can further this advantage to yourself by making your title interesting enough to get even more clicks. If your article makes it to the top in the search results but users dont click it as much, it will get removed fast. So making your title interesting and attention grabbing is as important as opening it with the HOT keywords. By making your title stand out with the BOLD keywords and catchy words, you will secure more click throughs than the competition. In the example above, you can see 'The Inquisitr' is listed above the 'The Huffington Post'. The title is much more interesting than the competition which in turn gets more click throughs. Search engines rely completly on such statistics to decide if they should keep your article on top or not. As an intelligent writer, you have to multiply to odds by making the best title possible.
<p>When choosing to write an article on 'Drew Brees', open the title with 'Drew Brees' and make it interesting. For example, "Drew Brees Breaks The All-Time Record in NFL History" or "Drew Brees Mind Boggling 5476 Yards Breaks All-Time NFL Records". If you have a writers block with titles, start with the HOT keywords then include one or two strong interesting angles from the story. Write the top exciting angles from the reader's prospect. Sell the article to the readers through the title. When readers share the article, the title will bring in even more interest and click throughs whenever it's shared.</p></td>
