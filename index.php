          <p> <strong>A:</strong> No. We ask that questions only be added if they are likely to engage readers and lead to comments. Meaningful well though out questions are appreciated. </p><br />
          <p> <strong>Q: Who chooses which one of my stories is featured in the big features section?</strong> </p>
          <p> <strong>A:</strong> Writers can feature their stories in sections but our editors choose which stories go in the slider section. </p><br />
          <p> <strong>Q: I see that I can choose which of my stories to "Secondary Feature" is that at my own discretion?</strong> </p>
          <p> <strong>A:</strong> Yes. </p><br />
          <p> <strong>Q: How are my articles edited?</strong> </p>
          <p> <strong>A:</strong> We use a pigeon pecking order approach. After you publish our editor will pick stories at random to edit. We ask that you double check your stories for mistakes before publishing. If a story is trending it is immediately picked for editing. </p><br />
          <p> <strong>Q: Do I need to request time off if I will be out of town or gone for a few days?</strong> </p>
          <p> <strong>A:</strong> Please tell a senior member when you will be gone for any period of time. </p><br />
          <p> <strong>Q: Can I write for other news publications while writing for Inquisitr?</strong> </p>
          <p> <strong>A:</strong> We do not ask for exclusivity at this time. </p><br />
          <p> <strong>Q: Can I publish a full "letter to employees" or memo's if they are long but add context to a story?</strong> </p>
          <p> <strong>A:</strong> If they are paramount for the story you can publish them in their entirety after you have written at least 250 words. </p><br />
          <p> <strong>Q: What is the longest length an article should be?</strong> </p>
          <p> <strong>A:</strong> There is no top limit but typically articles over 2,000 words are deemed by readers to be too long. If you write a very long story use sub-sections highlighted by the H2 tag. </p><br />
          <p> <strong>Q: Can I write a story based solely around speculation or rumors?</strong> </p>
          <p> <strong>A:</strong> As long as you note thoroughly that the news is based on rumors or speculation you are okay to write them. Many entertainment articles and political articles fall into this category. </p><br />
          <p> <strong>Q: Can I write a biased story if the information provided is all that is available but is believed to be accurate? How do I vet those stories?</strong> </p>
          <p> <strong>A:</strong> Yes, however be skeptical in your writing to show no outward journalist bias. </p><br />
          <p> <strong>Q: Can I write link roundup or quote stories that focus on reactions to news? For example: "Five Senators React To President Obama's Gun Control Speech"</strong> </p>
          <p> <strong>A:</strong> Yes, but please have a lengthy introduction to allow for originality in your article. </p><br />
          <p> <strong>Q: Is it okay to write stories about viral videos, viral speeches, and other viral examples that might not be deemed NEWS?</strong> </p>
          <p> <strong>A:</strong> YES! We have a buzzworthy section for this type of writing and if its news based it can go in the respective category. </p><br />
          <p> <strong>Q: If I find an interesting story from a year ago that didn't get a lot of coverage can I cover it? Or is Inquisitr focused on breaking news that is just happening?</strong> </p>
          <p> <strong>A:</strong> We are a breaking news website. </p><br />
          <p> <strong>Q: Can I choose to only cover one category? For example Technology or Lifestyle?</strong> </p>
          <p> <strong>A:</strong> You can choose to cover one category but be aware that failing to write trending articles may affect your pageviews. </p><br />
          <p> <strong>Q: What is IQ's policy on writing pieces that attack another websites Op-Ed and News pieces?</strong> </p>
          <p> <strong>A:</strong> If there is a story that is patently wrong or needs more examination you can write those pieces. We keep things civil at Inquisitr and ask that our writers use logical thought for these type of pieces. </p><br />
          <p> <strong>Q: Are there certain times of the day that are best for posting articles to gain pageviews?</strong> </p>
          <p> <strong>A:</strong> Early morning writing is crucial for pageviews. Typically 5am Eastern through 1pm Eastern are great times to write. However, writing trending articles at any time of the day is welcomed. Many of our writers start in the morning and then write throughout their day. </p><br />
          <p> <strong>Q: Can I write "Top 10" type pieces? Is there a certain category these should be placed in? Should they still be news or viral focused?</strong> </p>
          <p> <strong>A:</strong> Yes you can write these pieces and they can be placed in their respective categories. However, they should still be news focused in some way. For example, you can tie in a Top 10 Politicians Who Have Been Arrested to a recent political arrests sting. </p><br />
          <p> <strong>Q: Does IQ accept Interview submissions or Movie Reviews at higher pay? Who do I talk to about those pieces?</strong> </p>
          <p> <strong>A:</strong> We do accept these pieces and they must be approved by our sites editorial team. </p><br />
          <p> <strong>Q: What about original news pieces? If I want to do a longer piece that takes a few days is this an option with higher pay?</strong> </p>
          <p> <strong>A:</strong> Please discuss this type of content with our editorial team. </p><br />
          <p> <strong>Q: Can I engage with my readers in the comments section? Are there policies in place about how to talk to Internet trolls and others?</strong> </p>
          <p> <strong>A:</strong> Yes you can. We ask that you remain civil and don't outwardly attack commenters. </p><br />
          <p> <strong>Q: What do I do if I spot racist or threatening comments on IQ?</strong> </p>
          <p> <strong>A:</strong> Tell a member of the editorial team. </p><br />
          <p> <strong>Q: Is it okay if I get setup with a Pen Name? Would I need social accounts with that name as well?</strong> </p>
          <p> <strong>A:</strong> Your social accounts must match the pen name you choose to use, especially Google+ since your account name must match your pen name for Google Authorship purposes. </p><br />
          <p> <strong>Q: If I add an update to a story do I put "Update:" at the top or bottom of the story? Should I bold the Update term? How does IQ handle updates?</strong> </p>
          <p> <strong>A:</strong> Our style guide explains the full Update process. </p><br />
          <p> <strong>Q: If I have a rather large update can I just write a new story?</strong> </p>
          <p> <strong>A:</strong> Yes. </p><br />
          <p> <strong>Q: How should I like to at least two other IQ stories? Should I find a story just like it? Should I link on a single keyword or a keyword phrase (Long-Tail Keyword)?</strong> </p>
          <p> <strong>A:</strong> Linking to like-minded articles will help search engine placement and provide our readers with a much better experience. </p>
