        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use dashes to denote a higher level of emphasis<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use dashes to introduce explanations not part of the main clause<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use parentheses to indicate a moderate degree of emphasis<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use parentheses to enclose clarifications e.g. thirty thousand dollars ($30,000)<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use parentheses to enclose asides and additional information not part of the grammatical structure of the main clause<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use parentheses to enclose numbers or letters in a list</p>
      <p class="style10"><strong>12. Ellipses</strong><br />
        <br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use a space on both sides to offset an ellipsis ( &hellip;)</p>
      <p class="style10"><strong>13. Hyphens</strong><br />
        <br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use with compound numbers between twenty-one and ninety-nine<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use to separate in fractions (one-half)<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use with some compound nouns (mother-in-law)<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use with coequal nouns (writer-illustrator)<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use with compound modifiers before a noun (middle-class family)<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Don't use when the first noun modifies the second (child actor)<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use to join adjectives with adverbs (well-known actor)<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use where second word is present or past participle of a verb (hate-filled rhetoric)<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use if modifiers contain numbers (twentieth-century literature)<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Don't use if compound modifiers follow nouns (the author is well known)<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Don't use with adjectives or adverbs ending in -ly, or the adverbs too, very, or much.<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use if modifiers precede noun (all-you-can-eat buffet)<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use with certain prefixes ( all-purpose, mid-century etc.)<br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use to avoid misreading ( re-sign not resign)<br />
        <br />
      <strong>14. Apostrophes</strong><br />
      <br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Add before the s for single nouns provided the single form doesn't end in s (puppy's ball)<br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Add after final s if noun ends in s (walrus' tusks)<br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Add after the s plural marker (students' essays)<br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Add plus s before end of plural nouns not ending in s (mice's cage)<br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Place after final noun to show joint possession (mom and dad's house)<br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Place after each noun to show individual possession (mom's and dad's house)<br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Don't use with abbreviated decades (60s, 70s)<br />
        <br />
      <strong>15. Internet/Email</strong><br />
      <br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Don't capitalize the word internet<br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Email is one word with no hyphen</p>
      <p class="style10"><strong>16. Links</strong><br />
        <br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Link only keywords, not surrounding punctuation and spaces</p>
      <p class="style10"><strong>17. Collective nouns</strong><br />
        <br />
        <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Collective nouns take a singular verb (The <em>furniture</em> is expensive.)<br />
        <br />
      <strong>18. Abbreviations/ Periods</strong><br />
      <br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Don't abbreviate words (Pres. Obama), months, days of the week,or states (FL)<br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Don't use periods in US or UK<br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use lowercase for am pm without periods<br />
        <br />
      <strong>19. Swearing</strong><br />
      <br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Don't use swear words even if quoting another source<br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&#9679;</span> Use asterisks (f**k, a**hole)<br />
        <br />
      <strong>20. Use simple sentences</strong><br />
      <br />
      <span class="style11">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &#9679;</span> Simple sentences increase page viewing potential because they increase readability</p>    </td>
