    <td height="475" bgcolor="#FFFFFF">
      <p><strong>(1) Write sensational articles.</strong> Inject the maximum amount of curiosity into the readers minds and make them click to read the entire article. The best example of balanced sensational headlines you will find is on Yahoo front page. Read the featured stories headlines and you will see what I mean. Don't write a headline that every one else wrote. Changing the position of words in the headline doesn't make it unique, it just makes it barely different from the rest. You are still using the same words, just in different ways. Being sensational is all about shock value and being different. Apply that to your entire article and you will see a big difference in results.</p>
      <p><strong>(2) Include as many details in your article as possible.</strong> This will automatically help you create more word count and have high ratio of unique words in your article. Search engines like Google love this as much as people do. Describe colors, smells, sounds, tastes and feelings as detailed as possible. That's your shortcut to emotionally connect with the readers immediately. Also this helps you pack your article with full information that is well rounded. Was the car in the story just a car, or was it a red late model chevrolet corvette? You get the point.</p>
      <p><strong>(3) Don't write what everyone expects.</strong> This is possibly the top secret to the universe of writing. If you follow this advice you will never have to worry about duplicating stories EVEN if they are trends. If Jennifer Lopez did something at Emmy awards, every one is going to write about Jennifer Lopez at the Emmy awards, but no one is going to write about how her boyfriend is cheating on her (I don't know if he is, just example), or how her album sales are declining and she is facing bankruptcy (again, just example).. So while everyone is running to search for Jennifer Lopez and that is the highlight of their moment, you catch them with new information about Jennifer Lopez that they otherwise would have never known they would be interested in.. In a way you could possibly create a trend if your angle is unique and strong. Basically it's exploiting stories related to trending stories.</p>
      
