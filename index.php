<p>
Choosing a great picture to stand out from the rest of article is simple. Focus on being specific with your pictures. If the president of China met with President of USA for some tea time at the white house, then by all means post a picture of them having tea together. Do not post a picture of the country's flags, that's just too plain and broad. Do not post a picture of what Obama or Hu look like, it's irrelevant to the story. Post a picture that will speak what the title has told the reader. The more specific you are with the picture, the more credibility your article has. Readers know when the writer has spent 5 minutes finding a good picture or spent 5 seconds grabbing the first image from google.
